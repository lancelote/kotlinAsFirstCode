fun digitNumber(n: Int): Int = n.toString().length

/**
 * Простая
 *
 * Найти число Фибоначчи из ряда 1, 1, 2, 3, 5, 8, 13, 21, ... с номером n.
 * Ряд Фибоначчи определён следующим образом: fib(1) = 1, fib(2) = 1, fib(n+2) = fib(n) + fib(n+1)
 */
fun fib(n: Int): Int {
    var a = 1
    var b = 1
    var count = n - 2
    while (count > 0) {
        val tmp = a
        a = b
        b += tmp
        count--
    }
    return b
}

fun gcd(a: Int, b: Int): Int = if (b == 0) a else gcd(b, a % b)

/**
 * Простая
 *
 * Для заданных чисел m и n найти наименьшее общее кратное, то есть,
 * минимальное число k, которое делится и на m и на n без остатка
 */
fun lcm(m: Int, n: Int): Int = m * n / gcd(m, n)

/**
 * Простая
 *
 * Для заданного числа n > 1 найти минимальный делитель, превышающий 1
 */
fun minDivisor(n: Int): Int {
    for (i in 2..n)
        if (n % i == 0)
            return i
    return 0
}

/**
 * Простая
 *
 * Для заданного числа n > 1 найти максимальный делитель, меньший n
 */
fun maxDivisor(n: Int): Int {
    for (i in n-1 downTo 1)
        if (n % i == 0)
            return i
    return 0
}

/**
 * Простая
 *
 * Определить, являются ли два заданных числа m и n взаимно простыми.
 * Взаимно простые числа не имеют общих делителей, кроме 1.
 * Например, 25 и 49 взаимно простые, а 6 и 8 -- нет.
 */
fun isCoPrime(m: Int, n: Int): Boolean {
    for (i in 2..min(m, n))
        if (m % i == 0 && n % i == 0)
            return false
    return true
}

/**
 * Простая
 *
 * Для заданных чисел m и n, m <= n, определить, имеется ли хотя бы один точный квадрат между m и n,
 * то есть, существует ли такое целое k, что m <= k*k <= n.
 * Например, для интервала 21..28 21 <= 5*5 <= 28, а для интервала 51..61 квадрата не существует.
 */
fun squareBetweenExists(m: Int, n: Int): Boolean {
    val mSqrt = sqrt(n.toDouble())
    val nSqrt = sqrt(m.toDouble())

    if (m == n && mSqrt % 1.0 == 0.0)
        return true
    else
        return mSqrt.toInt() - nSqrt.toInt() > 0
}

fun factorial(n: Int): Double {
    var result = 1.0
    for (i in 1..n)
        result *= i
    return result
}

/**
 * Простая
 *
 * Для заданного x рассчитать с заданной точностью eps
 * sin(x) = x - x^3 / 3! + x^5 / 5! - x^7 / 7! + ...
 * Нужную точность считать достигнутой, если очередной член ряда меньше eps по модулю
 */
fun sin(x: Double, eps: Double): Double {
    var i = 1
    var sign = 1
    var sum = 0.0

    do {
        val item = sign * pow(x, i.toDouble()) / factorial(i)

        sum  += item
        i    += 2
        sign *= -1
    } while (abs(item) > eps)
    return sum
}

/**
 * Простая
 *
 * Для заданного x рассчитать с заданной точностью eps
 * cos(x) = 1 - x^2 / 2! + x^4 / 4! - x^6 / 6! + ...
 * Нужную точность считать достигнутой, если очередной член ряда меньше eps по модулю
 */
fun cos(x: Double, eps: Double): Double {
    var i = 0
    var sign = 1
    var sum = 0.0

    do {
        val item = sign * pow(x, i.toDouble()) / factorial(i)

        sum  += item
        i    += 2
        sign *= -1
    } while (abs(item) > eps)
    return sum
}

/**
 * Средняя
 *
 * Поменять порядок цифр заданного числа n на обратный: 13478 -> 87431.
 * Не использовать строки при решении задачи.
 */
fun revert(n: Int): Int {
    if (n < 10) return n
    else {
        val ones = n % 10
        val rest = n / 10
        val result = ones * pow(10.0, log10(rest.toDouble()).toInt() + 1.0) + revert(rest)
        return result.toInt()
    }
}

/**
 * Средняя
 *
 * Проверить, является ли заданное число n анаграммой:
 * первая цифра равна последней, вторая -- предпоследней и так далее.
 * 15751 -- палиндром, 3653 -- нет.
 */
fun isAnagram(n: Int): Boolean {
    return n.toString() == n.toString().reversed()
}

/**
 * Средняя
 *
 * Для заданного числа n определить, содержит ли оно различающиеся цифры.
 * Например, 54 и 323 состоят из разных цифр, а 111 и 0 из одинаковых.
 */
fun hasDifferentDigits(n: Int): Boolean {
    var stringNum = n.toString()
    for (i in 1..stringNum.length-1)
        if (stringNum[i-1] != stringNum[i])
            return true
    return false
}

